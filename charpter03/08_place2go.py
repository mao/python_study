places =['Paris', 'Florence', 'San Francisco', 'London', 'Australia', 'Venice']

print('\nOraginal:', places)
print(sorted(places))

print('\nOraginal:', places)
print(sorted(places, reverse=True))

print('\nOraginal:', places)
places.reverse()
print('Reversed:', places)
places.reverse()
print('2nd Reversed:', places)

places.sort()
print('\nSorted:', places)
places.sort(reverse = True)
print('Reverse sorted:', places)